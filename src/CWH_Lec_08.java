public class CWH_Lec_08 {
    public static void main(String[] args) {


        int a = 9;
        int b = 3;
        float c = 12;
        float d = 5;

//        Arithmetic operators
        System.out.println("Using (+) operator " + (a + b));
        System.out.println("Using (-) operator " + (a - b));
        System.out.println("Using (*) operator " + (a * b));
        System.out.println("Using (%) operator " + (c % d));

//        Comparison operator
        System.out.println("Comparison operator " + (a > b));

//        Logical operator
        System.out.println("Logical and operator " + (34 > 23 && 44 > 22));

        System.out.println("Logical or operator " + (45 < 34 || 34 > 22));


    }
}
